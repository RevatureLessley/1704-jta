/*******************************************************************************
   Create Tables
********************************************************************************/

CREATE TABLE hs_user (
id NUMBER NOT NULL,
username VARCHAR2(20) NOT NULL,
password VARCHAR2(60) NOT NULL,
is_locked CHAR(1)  DEFAULT '0' CHECK(is_locked in ('0', '1')),
role VARCHAR2(5) CHECK( role in ('admin', 'player')),
CONSTRAINT PK_USERID PRIMARY KEY (id)
);

CREATE TABLE player (
id NUMBER NOT NULL,
dust NUMBER,
is_approved CHAR(1) DEFAULT '0' CHECK(is_approved in ('0', '1')),
CONSTRAINT PK_PLAYERID PRIMARY KEY (id),
CONSTRAINT FK_PLAYERID FOREIGN KEY (id) REFERENCES hs_user(id)
);

CREATE TABLE card (
id VARCHAR(10) NOT NULL,
name VARCHAR(40),
rarity VARCHAR(10),
card_set VARCHAR(10),
CONSTRAINT PK_CARDID PRIMARY KEY (id)
);


CREATE TABLE player_card (
player_id NUMBER NOT NULL,
card_id VARCHAR(10) NOT NULL,
card_count NUMBER,
CONSTRAINT PK_IDS PRIMARY KEY(player_id, card_id),
CONSTRAINT FK_PLAYERID_CARD FOREIGN KEY (player_id) REFERENCES player(id),
CONSTRAINT FK_CARDID FOREIGN KEY (card_id) REFERENCES card(id)
);

